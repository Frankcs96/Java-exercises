
public class Dog extends Mammal {

  public Dog(String name) {
    super(name);
    
  }
  
  public void bark () {
    System.out.println("GUAU GUAU!");
  }
  
  public void moveTail () {
    System.out.println("Moving tail so fast because I'm happy");
  }
  
  public void gasp () {
    System.out.println("I'm tired....");
  }
  
}
